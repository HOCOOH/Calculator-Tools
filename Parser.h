#ifndef PARSER_H
#define PARSER_H

#include "Lexer.h"

/*----------------------------------------------------------------
program → decls stmts
----------------------------------------------------------------*/
void Parser();
/* ----------------------------------------------------------------
decls → decls decl | ε
decl → type id ; | type id = expr ;
---------------------------------------------------------------- */
void decls();
/* ----------------------------------------------------------------
stmts → stmts stmt | ε
stmt → id = expr ; | write ( expr ) ;
---------------------------------------------------------------- */
void stmts();
/* ----------------------------------------------------------------
expr → expr + term | expr - term | term
---------------------------------------------------------------- */
Token expr();
/* ----------------------------------------------------------------
term → term * unary | term / unary | unary
---------------------------------------------------------------- */
Token term();
/* ----------------------------------------------------------------
unary → - unary | factor
---------------------------------------------------------------- */
Token unary();
/* ----------------------------------------------------------------
factor → (expr) | number | id
---------------------------------------------------------------- */
Token factor();

// left = expr ;
void Assign(Token* left);
// 匹配一个单字符Token
void match(int tag);

#endif