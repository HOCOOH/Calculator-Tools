#ifndef PARSER_H
#define PARSER_H

#include "Lexer.h"

/*----------------------------------------------------------------
program → decls stmts
----------------------------------------------------------------*/
void Parser();
/* ----------------------------------------------------------------
decls → decls decl | ε
decl → type id ; | type id = expr ;
---------------------------------------------------------------- */
void decls();
/* ----------------------------------------------------------------
stmts → stmts stmt | ε
stmt → id = expr ; | write ( expr ) ;
---------------------------------------------------------------- */
void stmts();
/* ----------------------------------------------------------------
expr → expr + term | expr - term | term
---------------------------------------------------------------- */
Token expr();
/* ----------------------------------------------------------------
term → term * factor | term / factor | factor
---------------------------------------------------------------- */
Token term();

Token unary();

/* ----------------------------------------------------------------
factor → (expr) | num | real | id
---------------------------------------------------------------- */
Token factor();
// 匹配一个单字符Token
void match(int tag);
// left = expr ;
void Assign(Token* left);

#endif